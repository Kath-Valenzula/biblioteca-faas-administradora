ALTER SESSION SET CONTAINER = FREEPDB1;

CREATE TABLE BIBLIOTECA.USUARIOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE VARCHAR2(120) NOT NULL,
    CORREO VARCHAR2(120) NOT NULL,
    TELEFONO VARCHAR2(30),
    ESTADO VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,
    FECHA_REGISTRO DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT UK_USUARIOS_CORREO UNIQUE (CORREO),
    CONSTRAINT CK_USUARIOS_ESTADO CHECK (ESTADO IN ('ACTIVO', 'INACTIVO'))
);

CREATE TABLE BIBLIOTECA.LIBROS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITULO VARCHAR2(150) NOT NULL,
    AUTOR VARCHAR2(120) NOT NULL,
    ISBN VARCHAR2(20) NOT NULL,
    ESTADO VARCHAR2(20) DEFAULT 'DISPONIBLE' NOT NULL,
    DESCRIPCION VARCHAR2(500),
    CONSTRAINT UK_LIBROS_ISBN UNIQUE (ISBN),
    CONSTRAINT CK_LIBROS_ESTADO CHECK (ESTADO IN ('DISPONIBLE', 'PRESTADO'))
);

CREATE TABLE BIBLIOTECA.PRESTAMOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USUARIO_ID NUMBER NOT NULL,
    LIBRO_ID NUMBER NOT NULL,
    FECHA_PRESTAMO DATE NOT NULL,
    FECHA_DEVOLUCION_ESTIMADA DATE NOT NULL,
    FECHA_DEVOLUCION_REAL DATE,
    ESTADO VARCHAR2(20) DEFAULT 'ACTIVO' NOT NULL,
    OBSERVACION VARCHAR2(500),
    FECHA_REGISTRO DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_PRESTAMO_USUARIO FOREIGN KEY (USUARIO_ID) REFERENCES BIBLIOTECA.USUARIOS(ID),
    CONSTRAINT FK_PRESTAMO_LIBRO FOREIGN KEY (LIBRO_ID) REFERENCES BIBLIOTECA.LIBROS(ID),
    CONSTRAINT CK_PRESTAMOS_ESTADO CHECK (ESTADO IN ('ACTIVO', 'DEVUELTO')),
    CONSTRAINT CK_PRESTAMOS_FECHAS CHECK (FECHA_DEVOLUCION_ESTIMADA >= FECHA_PRESTAMO)
);

CREATE INDEX BIBLIOTECA.IDX_PRESTAMOS_USUARIO ON BIBLIOTECA.PRESTAMOS (USUARIO_ID);
CREATE INDEX BIBLIOTECA.IDX_PRESTAMOS_LIBRO ON BIBLIOTECA.PRESTAMOS (LIBRO_ID);
CREATE INDEX BIBLIOTECA.IDX_LIBROS_ESTADO ON BIBLIOTECA.LIBROS (ESTADO);
